export const getBgColor = (type) => {
  switch (type) {
    case "fire":
      return "bg-fire";
    case "water":
      return "bg-water";
    case "grass":
      return "bg-grass";
    case "bug":
      return "bg-bug";
    case "fairy":
      return "bg-fairy";
    case "normal":
      return "bg-normal";
    case "dragon":
      return "bg-dragon";
    case "psychic":
      return "bg-psychic";
    case "ghost":
      return "bg-ghost";
    case "ground":
      return "bg-ground";
    case "steel":
      return "bg-steel";
    case "flying":
      return "bg-flying";
    case "ice":
      return "bg-ice";
    case "electric":
      return "bg-electric";
    case "rock":
      return "bg-rock";
    case "dark":
      return "bg-dark";
    case "fighting":
      return "bg-fighting";
    case "poison":
      return "bg-poison";
    default:
      return "bg-other";
  }
};

